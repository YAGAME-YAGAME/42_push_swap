#include "../../include/push_swap_bonus.h"

void pa(t_stack **a, t_stack **b)
{
    t_stack *tmp;

    if (!b || !*b)
        return ;
    tmp = (*b)->next;
    (*b)->next = *a;
    *a = *b;
    *b = tmp;
}