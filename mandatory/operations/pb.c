#include "../../include/push_swap.h"

void pb(t_stack **a, t_stack **b)
{
    t_stack *tmp;

    if (!a || !*a)
        return ;
    tmp = (*a)->next;
    (*a)->next = *b;
    *b = *a;
    *a = tmp;
    write(1, "pb\n", 3);
}